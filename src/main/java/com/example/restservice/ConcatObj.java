package com.example.restservice;

class ConcatObj {
    private final String str1;
    private final String str2;


    public ConcatObj(String str1, String str2) {
        this.str1 = str1;
        this.str2 = str2;
    }

    public String getStr1() {
        return str1;
    }


    public String getStr2() {
        return str2;
    }

}
